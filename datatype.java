public class datatype {
    public static void main(String[] args) {
        byte x=5;
        int y=6;
        short z=10;
        int a=y+z;//data type int
        float b=6.4f+a;
        System.out.println(a);
        System.out.println(b);






        /*
          X=byte+short-->int
          X=short+int-->int
          X=long+float-->float
          X=int+float-->float
          X=long+int-->int
          X=long+short-->int
          X=long+double-->double
          X=float+double-->double
         */
    }
}
